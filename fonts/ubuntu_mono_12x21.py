def draw_char(lcd, c, top, left):
    code_width = 16
    if c not in font:
        c = 'invalid'
    arr = font[c]
    for row in range(21):
        code = arr[row]
        for col in range(code_width, -1, -1):
            if code & (1 << col):
                lcd.pixel(left + (code_width-col), top+row, lcd.white)
                
def draw_word(lcd, word, top, left):
    letter_width = 12
    for c in word:
        draw_char(lcd, c, top, left)
        left += letter_width